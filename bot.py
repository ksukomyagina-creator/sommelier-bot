async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """Команда /start"""
    user = update.effective_user
    await update.message.reply_text(
        f"🍷 *Привет, {user.first_name}!*\n\n"
        f"Я профессиональный бот-сомелье с базой знаний более 50 вин.\n\n"
        f"🔍 *Что я умею:*\n"
        f"• Подбирать гастропары к любому вину\n"
        f"• Помогать с выбором вина к еде\n"
        f"• Давать профессиональные рекомендации\n\n"
        f"👇 *Выбери вино из списка:*",
        parse_mode='Markdown',
        reply_markup=get_wine_keyboard()
    )

async def handle_message(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """Обработка всех сообщений"""
    text = update.message.text.strip()
    text_lower = text.lower()
    
    # Навигация по меню
    if text == "🏠 Главное меню":
        await update.message.reply_text(
            "🍷 Главное меню. Выбери вино:",
            reply_markup=get_wine_keyboard()
        )
        return
    
    if text == "🔍 Поиск по еде":
        await update.message.reply_text(
            "🔍 *Поиск по продуктам*\n\n"
            "Выбери категорию:",
            parse_mode='Markdown',
            reply_markup=get_food_keyboard()
        )
        return
    
    if text == "📋 Категории":
        await update.message.reply_text(
            "📋 *Категории вин*\n\n"
            "• Красные вина\n"
            "• Белые вина\n"
            "• Игристые вина\n"
            "• Крепленые вина\n"
            "• Розовые вина\n"
            "• Оранжевые вина\n\n"
            "Просто напиши название вина!",
            parse_mode='Markdown',
            reply_markup=get_wine_keyboard()
        )
        return
    
    # Поиск по категориям еды
    if text == "🧀 Сыры":
        msg = "🧀 *Сочетания с сырами*\n\n"
        for cheese, wine in FOOD_PAIRINGS["сыры"]["pairs"].items():
            msg += f"• {cheese.title()}: {wine}\n"
        await update.message.reply_text(msg, parse_mode='Markdown', reply_markup=get_food_keyboard())
        return
    
    if text == "🐟 Рыба":
        msg = "🐟 *Сочетания с рыбой и морепродуктами*\n\n"
        for fish, wine in FOOD_PAIRINGS["рыба"]["pairs"].items():
            msg += f"• {fish.title()}: {wine}\n"
        await update.message.reply_text(msg, parse_mode='Markdown', reply_markup=get_food_keyboard())
        return
    
    if text == "🍖 Мясо":
        msg = "🍖 *Сочетания с мясом*\n\n"
        for meat, wine in FOOD_PAIRINGS["мясо"]["pairs"].items():
            msg += f"• {meat.title()}: {wine}\n"
        await update.message.reply_text(msg, parse_mode='Markdown', reply_markup=get_food_keyboard())
        return
    
    # Поиск вина в базе
    found = False
    for wine_name, wine_info in WINES.items():
        if wine_name in text_lower or text_lower in wine_name:
            response = (
                f"🍷 *{wine_name.title()}*\n"
                f"_{wine_info['desc']}_\n\n"
                f"🍽 *Идеальная гастропара:*\n{wine_info['pair']}"
            )
            await update.message.reply_text(
                response,
                parse_mode='Markdown',
                reply_markup=get_wine_keyboard()
            )
            found = True
            break
    
    if not found and text not in ["🏠 Главное меню", "🔍 Поиск по еде", "📋 Категории", 
                                   "🧀 Сыры", "🐟 Рыба", "🍖 Мясо"]:
        await update.message.reply_text(
            "😕 Вино не найдено. Попробуй выбрать из списка или поищи по категориям еды!",
            reply_markup=get_wine_keyboard()
        )


# СОЗДАНИЕ КЛАВИАТУРЫ
def get_wine_keyboard():
    """Создает клавиатуру с названиями вин (сгруппировано по категориям)"""
    wines = sorted(WINES.keys())
    keyboard = []
    row = []
    
    for i, wine in enumerate(wines):
        # Красивое название с заглавной буквы
        display_name = wine.title()
        # Ограничиваем длину для компактности
        if len(display_name) > 15:
            display_name = display_name[:15] + "."
        row.append(display_name)
        
        # По 2 кнопки в ряд
        if (i + 1) % 2 == 0:
            keyboard.append(row)
            row = []
    
    if row:
        keyboard.append(row)
    
    # Добавляем категории
    keyboard.append(["🔍 Поиск по еде", "📋 Категории"])
    return ReplyKeyboardMarkup(keyboard, resize_keyboard=True)

def get_food_keyboard():
    """Клавиатура для поиска по еде"""
    keyboard = [
        ["🧀 Сыры", "🐟 Рыба"],
        ["🍖 Мясо", "🏠 Главное меню"]
    ]
    return ReplyKeyboardMarkup(keyboard, resize_keyboard=True)

